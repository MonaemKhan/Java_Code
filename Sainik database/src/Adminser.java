    private void printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printActionPerformed

        JFileChooser jf = new JFileChooser();
        jf.setCurrentDirectory(new File("user.home"));
        jf.setSelectedFile(new File("Info" + ".pdf"));

        int re = jf.showSaveDialog(null);
        if (re == JFileChooser.APPROVE_OPTION) {
            String path = jf.getSelectedFile().getAbsolutePath();
            try {
                Document document = new Document();

                PdfWriter.getInstance(document, new FileOutputStream(path));

                document.open();

                String s1
                        = "===========Army Person Information============\n"
                        + "           ID :: " + id.getText() + "\n"
                        + "         Name :: " + rank.getSelectedItem() + " " + name.getText() + "\n"
                        + "  Father Name :: " + father.getText() + "\n"
                        + "  Mother Name :: " + mother.getText() + "\n"
                        + "Bate Of Birth :: " + dob.getText() + "\n"
                        + "        Gender:: " + gender.getSelectedItem() + "\n"
                        + "  Blood Group :: " + blood.getSelectedItem() + "\n"
                        + "Home District :: " + hom.getSelectedItem() + "\n"
                        + " National ID  :: " + nation.getText() + "\n"
                        + "        Email :: " + email.getText() + "\n"
                        + "       Adress :: " + address.getText() + "\n"
                        + "         Core :: " + core.getSelectedItem() + "\n"
                        + "       Region :: " + region.getText() + "\n"
                        + "         Unit :: " + unit.getText() + "\n"
                        + "     Join Date:: " + joinda.getText() + "\n";
                Paragraph chunk = new Paragraph(s1, FontFactory.getFont(FontFactory.TIMES, 20, java.awt.Font.PLAIN));
                document.add(chunk);
                document.close();

                JOptionPane.showMessageDialog(null, "File Save Successfull");
            } catch (FileNotFoundException | DocumentException ex) {
                System.out.println(ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "File Save Unsuccessfull !!!");
        }

    }//GEN-LAST:event_printActionPerformed
    public ImageIcon ResizeImage(String ImagePath) {
        ImageIcon MyImage = new ImageIcon(ImagePath);
        Image img = MyImage.getImage();
        Image newImg = img.getScaledInstance(snk_img.getWidth(), snk_img.getHeight(), Image.SCALE_SMOOTH);
        Snk_Image = new ImageIcon(newImg);
        return Snk_Image;
    }

