    private void idActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idActionPerformed
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/sainik_data", "root", "");

            String info = "SELECT * FROM `info` WHERE `id` ='" + id.getText() + "'";

            PreparedStatement info1 = conn.prepareStatement(info);

            ResultSet rs = info1.executeQuery();

            while (rs.next()) {
                name.setText(rs.getString("name"));
                father.setText(rs.getString("father"));
                mother.setText(rs.getString("mother"));
                core.setSelectedItem(rs.getString("core"));
                rank.setSelectedItem(rs.getString("rank"));
                gender.setSelectedItem(rs.getString("gender"));
                dob.setText(rs.getString("dob"));
                email.setText(rs.getString("email"));
                nation.setText(rs.getString("national"));
                blood.setSelectedItem(rs.getString("blood"));
                hom.setSelectedItem(rs.getString("home"));
                unit.setText(rs.getString("unit"));
                phn.setText(rs.getString("phn"));
                region.setText(rs.getString("region"));
                joinda.setText(rs.getString("join"));
                address.setText(rs.getString("address"));

                snk_img.setIcon(ResizeImage(rs.getString("img")));

            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_idActionPerformed

