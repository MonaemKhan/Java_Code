    private void browseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseActionPerformed
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filt = new FileNameExtensionFilter("*.images", "*.jpg", "*.png", "*.gif");
        file.addChoosableFileFilter(filt);
        int result = file.showSaveDialog(null);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = file.getSelectedFile();
            path = selectedFile.getAbsolutePath();
            snk_img.setIcon(ResizeImage(path));
        } else {
            JOptionPane.showMessageDialog(null, "No File Selected");
        }
    }//GEN-LAST:event_browseActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/sainik_data", "root", "");

            String query = "UPDATE `info` SET `img`=?,`name`=?,`father`=?,`mother`=?,`home`=?,`gender`=?,`email`=?,"
                    + "`national`=?,`dob`=?,`blood`=?,`core`=?,`rank`=? WHERE`id` ='" + id.getText() + "'";
            PreparedStatement pp = conn.prepareStatement(query);

            pp.setString(1, path);
            pp.setString(2, name.getText());
            pp.setString(3, father.getText());
            pp.setString(4, mother.getText());
            pp.setString(5, hom.getSelectedItem().toString());
            pp.setString(6, gender.getSelectedItem().toString());
            pp.setString(7, email.getText());
            pp.setString(8, nation.getText());
            pp.setString(9, dob.getText());
            pp.setString(10, blood.getSelectedItem().toString());
            pp.setString(11, core.getSelectedItem().toString());
            pp.setString(12, rank.getSelectedItem().toString());

            pp.executeUpdate();

            JOptionPane.showMessageDialog(null, "Successfully Updated");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_updateActionPerformed

