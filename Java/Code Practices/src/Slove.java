
import java.util.*;

public class Slove {
 public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int t = scan.nextInt();
        while (t > 0) {
            String str = scan.nextLine();
            String st  = str.toUpperCase();
            System.out.println(st);
            t--;
        }

    }
}